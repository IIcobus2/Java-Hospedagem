package br.com.prog2.rh.model;

public class Chale {
	private String localizacao;
	private int capacidade;
	private int valorAltaEst;
	private int valorBaixaEst;
	
	public String getLocalizacao() {
		return localizacao;
	}
	public void setLocalizacao(String localizacao) {
		this.localizacao = localizacao;
	}
	public int getCapacidade() {
		return capacidade;
	}
	public void setCapacidade(int capacidade) {
		this.capacidade = capacidade;
	}
	public int getValorAltaEst() {
		return valorAltaEst;
	}
	public void setValorAltaEst(int valorAltaEst) {
		this.valorAltaEst = valorAltaEst;
	}
	public int getValorBaixaEst() {
		return valorBaixaEst;
	}
	public void setValorBaixaEst(int valorBaixaEst) {
		this.valorBaixaEst = valorBaixaEst;
	}
	
	
}
